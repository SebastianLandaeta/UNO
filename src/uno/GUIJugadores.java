    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        if (textboxJugador.getText().isEmpty()) { //si el campo de texto no est√° vacio se van cargando los jugadores
            JLabel mensaje = new JLabel("Por favor inserte un nombre");
            mensaje.setFont(new Font("Arial", Font.BOLD, 48));
            JOptionPane.showMessageDialog(null, mensaje);
        } else {
            String nombre = textboxJugador.getText().trim();
            if (idJugadoresLabel.contains(nombre)) {
                JLabel mensaje = new JLabel("Este nombre ya ha sido introducido!");
                mensaje.setFont(new Font("Arial", Font.BOLD, 48));
                JOptionPane.showMessageDialog(null, mensaje);
                textboxJugador.setText("");
            } else {
                idJugadoresLabel.add(nombre);
                switch (idJugadoresLabel.size()) {
                    case 1:
                        jugador1.setText(idJugadoresLabel.get(0));
                        break;
                    case 2:
                        jugador1.setText(idJugadoresLabel.get(0));
                        jugador2.setText(idJugadoresLabel.get(1));
                        break;
                    case 3:
                        jugador1.setText(idJugadoresLabel.get(0));
                        jugador2.setText(idJugadoresLabel.get(1));
                        jugador3.setText(idJugadoresLabel.get(2));
                        break;
                    case 4:
                        jugador1.setText(idJugadoresLabel.get(0));
                        jugador2.setText(idJugadoresLabel.get(1));
                        jugador3.setText(idJugadoresLabel.get(2));
                        jugador4.setText(idJugadoresLabel.get(3));
                        break;
                    case 5:
                        jugador1.setText(idJugadoresLabel.get(0));
                        jugador2.setText(idJugadoresLabel.get(1));
                        jugador3.setText(idJugadoresLabel.get(2));
                        jugador4.setText(idJugadoresLabel.get(3));
                        jugador5.setText(idJugadoresLabel.get(4));
                        break;
                    case 6:
                        jugador1.setText(idJugadoresLabel.get(0));
                        jugador2.setText(idJugadoresLabel.get(1));
                        jugador3.setText(idJugadoresLabel.get(2));
                        jugador4.setText(idJugadoresLabel.get(3));
                        jugador5.setText(idJugadoresLabel.get(4));
                        jugador6.setText(idJugadoresLabel.get(5));
                        break;
                    case 7:
                        jugador1.setText(idJugadoresLabel.get(0));
                        jugador2.setText(idJugadoresLabel.get(1));
                        jugador3.setText(idJugadoresLabel.get(2));
                        jugador4.setText(idJugadoresLabel.get(3));
                        jugador5.setText(idJugadoresLabel.get(4));
                        jugador6.setText(idJugadoresLabel.get(5));
                        jugador7.setText(idJugadoresLabel.get(6));
                        break;
                    case 8:
                        jugador1.setText(idJugadoresLabel.get(0));
                        jugador2.setText(idJugadoresLabel.get(1));
                        jugador3.setText(idJugadoresLabel.get(2));
                        jugador4.setText(idJugadoresLabel.get(3));
                        jugador5.setText(idJugadoresLabel.get(4));
                        jugador6.setText(idJugadoresLabel.get(5));
                        jugador7.setText(idJugadoresLabel.get(6));
                        jugador8.setText(idJugadoresLabel.get(7));
                        break;
                    case 9:
                        jugador1.setText(idJugadoresLabel.get(0));
                        jugador2.setText(idJugadoresLabel.get(1));
                        jugador4.setText(idJugadoresLabel.get(3));
                        jugador5.setText(idJugadoresLabel.get(4));
                        jugador6.setText(idJugadoresLabel.get(5));
                        jugador7.setText(idJugadoresLabel.get(6));
                        jugador8.setText(idJugadoresLabel.get(7));
                        jugador9.setText(idJugadoresLabel.get(8));
                        break;
                    case 10:
                        jugador1.setText(idJugadoresLabel.get(0));
                        jugador2.setText(idJugadoresLabel.get(1));
                        jugador4.setText(idJugadoresLabel.get(3));
                        jugador5.setText(idJugadoresLabel.get(4));
                        jugador6.setText(idJugadoresLabel.get(5));
                        jugador7.setText(idJugadoresLabel.get(6));
                        jugador8.setText(idJugadoresLabel.get(7));
                        jugador9.setText(idJugadoresLabel.get(8));
                        jugador10.setText(idJugadoresLabel.get(9));
                        break;
                    default:
                        break;
                }

                if (!idJugadoresLabel.isEmpty() && idJugadoresLabel.size() < 11) {
                    JLabel mensaje = new JLabel("Se ha guardado al jugador!");
                    mensaje.setFont(new Font("Arial", Font.BOLD, 48));
                    JOptionPane.showMessageDialog(null, mensaje);
                    textboxJugador.setText("");
                }
                if (idJugadoresLabel.size() == 11) { //no pueden cargarse mas de 10
                    idJugadoresLabel.remove(nombre);
                    JLabel mensaje = new JLabel("Se han guardado el maximo de jugadores posibles(10)!");
                    mensaje.setFont(new Font("Arial", Font.BOLD, 48));
                    JOptionPane.showMessageDialog(null, mensaje);
                    textboxJugador.setText("");
                }

            }

        }
    }//GEN-LAST:event_botonGuardarActionPerformed

    private void botonEmpezarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEmpezarActionPerformed
        if (idJugadoresLabel.size() == 1 || idJugadoresLabel.size() == 0) {
            JLabel mensaje = new JLabel("Se requiere almenos 2 jugadores para jugar!");
            mensaje.setFont(new Font("Arial", Font.BOLD, 48));
            JOptionPane.showMessageDialog(null, mensaje);
            textboxJugador.setText("");
        } else {
            this.dispose();
            new GUIJuego(idJugadoresLabel, true).setVisible(true);
        }
    }//GEN-LAST:event_botonEmpezarActionPerformed

