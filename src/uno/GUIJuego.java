    public void setIconoBoton() { //este método asigna los iconos de sus respectivas cartas a los botones 
        String mano = juego.getManoJugador(juego.getJugadorActual()).stream().map(Object::toString).collect(Collectors.joining(","));
        String[] pngNombres = mano.split(",");
        idCartas = new ArrayList<>(Arrays.asList(pngNombres));
        for (int i = 0; i < idCartas.size(); i++) {
            botonesCartas.get(i).setIcon(new javax.swing.ImageIcon(getClass().getResource("/uno/Imagenes2/" + idCartas.get(i) + ".png")));
        }
        for (int i = idCartas.size(); i < botonesCartas.size(); i++) {
            botonesCartas.get(i).setIcon(null);
        }
    }

