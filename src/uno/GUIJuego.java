    //botón cargarse carta
    private void botonCargarseCartaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCargarseCartaActionPerformed

        if (juego.getCarga() == false) { //comprueba que este jugador no se haya ya cargado una carta en este turno
            try {
                juego.sacarCarta2(juego.getJugadorActual());
                juego.setCarga(true);
            } catch (InvalidColorException | InvalidNumeroException | InvalidTurnoException e) {
                Logger.getLogger(GUIJuego.class.getName()).log(Level.SEVERE, null, e);
            }
        } else { //si intentas cargarte mas de una carta te salta tu turno
            JLabel mensaje = new JLabel(juego.getJugadorActual() + " ya se cargó una carta!");
            mensaje.setFont(new Font("Arial", Font.BOLD, 48));
            JOptionPane.showMessageDialog(null, mensaje);
            juego.turno();
        }

        this.setId(juego.getJugadorActual());
        this.setIconoBoton();

    }//GEN-LAST:event_botonCargarseCartaActionPerformed

